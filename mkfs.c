// convert to intel byte order
ushort
xshort(ushort x)
{
  ushort y;
  uchar *a = (uchar*)&y;
  a[0] = x;
  a[1] = x >> 8;
  return y;
}

uint
xint(uint x)
{
  uint y;
  uchar *a = (uchar*)&y;
  a[0] = x;
  a[1] = x >> 8;
  a[2] = x >> 16;
  a[3] = x >> 24;
  return y;
}

int
write_kernel(char* kernel_file)
{
  char buf[BSIZE];

  // Open kernel file
  int kernel_fd = open(kernel_file, O_RDONLY, 0666);
  if(kernel_fd < 0){
    perror(kernel_file);
    exit(1);
  }

  // Get kernel size (bytes)
  off_t fsize;
  fsize = lseek(kernel_fd, 0, SEEK_END);
  lseek(kernel_fd, 0, SEEK_SET);

  int kernel_size = fsize;
  printf("kernel file size = %d\n", kernel_size);
  
  // Copy block after block
  int block_offset = 1; // first kernel block is 1
  while (kernel_size > 0) {
    // Determine size to copy
    int copy_size = (kernel_size > BSIZE) ? BSIZE : kernel_size;
    memset(buf, 0, BSIZE);
    read(kernel_fd, buf, copy_size);
    wsect(block_offset, buf);
    // Update next block and size left
    ++block_offset;
    kernel_size -= copy_size;
  }

  printf("kernel last block = %d\n", block_offset - 1);
  return block_offset - 1; // return the last block of kernel
}

int
main(int argc, char *argv[])
{
  int i, cc, fd;
  uint rootino, inum, off;
  struct dirent de;
  char buf[BSIZE];
  struct dinode din;

  static_assert(sizeof(int) == 4, "Integers must be 4 bytes!");

  if(argc < 4){
    fprintf(stderr, "Usage: mkfs fs.img files...\n");
    exit(1);
  }

  assert((BSIZE % sizeof(struct dinode)) == 0);
  assert((BSIZE % sizeof(struct dirent)) == 0);

  fsfd = open(argv[1], O_RDWR|O_CREAT|O_TRUNC, 0666);
  if(fsfd < 0){
    perror(argv[1]);
    exit(1);
  }

  int bootbl_fd = open(argv[2], O_RDONLY, 0666);
  if(bootbl_fd < 0){
    perror(argv[2]);
    exit(1);
  }

  memset(mbrbl.bootstrap, 0, BOOTSTRAP);
  read(bootbl_fd, mbrbl.bootstrap, BOOTSTRAP);

  int last_kernel_block = write_kernel(argv[3]);

  //setup first partition
  struct dpartition* curr_partition = &(mbrbl.partitions[0]);
  curr_partition->flags = PART_ALLOCATED;
  curr_partition->type = FS_INODE;
  curr_partition->offset = last_kernel_block + 1; // bob
  curr_partition->size = FSSIZE;

  //create mbr and write it to disk
  for (i = 1; i < NPARTITIONS; i++)
  {
    mbrbl.partitions[i].flags = 0;      // make sure that all other partition flags are set to not allocated
  }
  mbrbl.magic[0] = 0x55;                    
  mbrbl.magic[1] = 0xAA;                //set magic numbers

  uint currpr_offset = curr_partition->offset;
  // 1 fs block = 1 disk sector
  nmeta = 1 + nlog + ninodeblocks + nbitmap;
  nblocks = FSSIZE - nmeta;

  sb.size = xint(FSSIZE);
  sb.nblocks = xint(nblocks);
  sb.ninodes = xint(NINODES);
  sb.nlog = xint(nlog);
  sb.logstart = xint(currpr_offset + 1);
  sb.inodestart = xint(currpr_offset + 1 + nlog);
  sb.bmapstart = xint(currpr_offset + 1 + nlog + ninodeblocks);

  printf("nmeta %d (boot, super, log blocks %u inode blocks %u, bitmap blocks %u) blocks %d total %d\n",
         nmeta, nlog, ninodeblocks, nbitmap, nblocks, FSSIZE);

  freeblock = currpr_offset + nmeta;     // the first free block that we can allocate

  //zero all partition
  for(i = 0; i < FSSIZE; i++)
    wsect(currpr_offset + i, zeroes);

  memset(buf, 0, sizeof(buf));
  memmove(buf, &sb, sizeof(sb));
  wsect(currpr_offset, buf);

  rootino = ialloc(T_DIR);
  assert(rootino == ROOTINO);

  bzero(&de, sizeof(de));
  de.inum = xshort(rootino);
  strcpy(de.name, ".");
  iappend(rootino, &de, sizeof(de));

  bzero(&de, sizeof(de));
  de.inum = xshort(rootino);
  strcpy(de.name, "..");
  iappend(rootino, &de, sizeof(de));

  int sh_exist = 0;
  int init_exist = 0;

  for(i = 4; i < argc; i++){
    assert(index(argv[i], '/') == 0);

    if((fd = open(argv[i], 0)) < 0){
      perror(argv[i]);
      exit(1);
    }
    
    // Skip leading _ in name when writing to file system.
    // The binaries are named _rm, _cat, etc. to keep the
    // build operating system from trying to execute them
    // in place of system binaries like rm and cat.
    if(argv[i][0] == '_')
      ++argv[i];

    inum = ialloc(T_FILE);

    bzero(&de, sizeof(de));
    de.inum = xshort(inum);
    strncpy(de.name, argv[i], DIRSIZ);
    iappend(rootino, &de, sizeof(de));

    if (strlen(argv[i]) == 2 && strncmp(argv[i], "sh", 2) == 0)
      sh_exist = 1;
    else if (strlen(argv[i]) == 4 && strncmp(argv[i], "init", 4) == 0)
      init_exist = 1;

    while((cc = read(fd, buf, sizeof(buf))) > 0)
      iappend(inum, buf, cc);

    close(fd);
  }

  if (sh_exist && init_exist)
    curr_partition->flags |= PART_BOOTABLE;
  
  memset(buf, 0, sizeof(buf));
  memmove(buf, &mbrbl, sizeof(mbrbl));
  wsect(0, buf);                        // write mbr to disk block 0

  // fix size of root inode dir
  rinode(rootino, &din);
  off = xint(din.size);
  off = ((off/BSIZE) + 1) * BSIZE;
  din.size = xint(off);
  winode(rootino, &din);

  balloc(freeblock);

  exit(0);
}

void
wsect(uint sec, void *buf)
{
  if(lseek(fsfd, sec * BSIZE, 0) != sec * BSIZE){
    perror("lseek");
    exit(1);
  }
  if(write(fsfd, buf, BSIZE) != BSIZE){
    perror("write");
    exit(1);
  }
}

void
winode(uint inum, struct dinode *ip)
{
  char buf[BSIZE];
  uint bn;
  struct dinode *dip;

  bn = IBLOCK(inum, sb);
  rsect(bn, buf);
  dip = ((struct dinode*)buf) + (inum % IPB);
  *dip = *ip;
  wsect(bn, buf);
}

void
rinode(uint inum, struct dinode *ip)
{
  char buf[BSIZE];
  uint bn;
  struct dinode *dip;

  bn = IBLOCK(inum, sb);
  rsect(bn, buf);
  dip = ((struct dinode*)buf) + (inum % IPB);
  *ip = *dip;
}

void
rsect(uint sec, void *buf)
{
  if(lseek(fsfd, sec * BSIZE, 0) != sec * BSIZE){
    perror("lseek");
    exit(1);
  }
  if(read(fsfd, buf, BSIZE) != BSIZE){
    perror("read");
    exit(1);
  }
}

uint
ialloc(ushort type)
{
  uint inum = freeinode++;
  struct dinode din;

  bzero(&din, sizeof(din));
  din.type = xshort(type);
  din.nlink = xshort(1);
  din.size = xint(0);
  winode(inum, &din);
  return inum;
}

void
balloc(int used)
{
  uchar buf[BSIZE];
  int i;

  printf("balloc: first %d blocks have been allocated\n", used);
  assert(used < BSIZE*8);
  bzero(buf, BSIZE);
  for(i = 0; i < used; i++){
    buf[i/8] = buf[i/8] | (0x1 << (i%8));
  }
  printf("balloc: write bitmap block at sector %d\n", sb.bmapstart);
  wsect(sb.bmapstart, buf);
}

#define min(a, b) ((a) < (b) ? (a) : (b))

void
iappend(uint inum, void *xp, int n)
{
  char *p = (char*)xp;
  uint fbn, off, n1;
  struct dinode din;
  char buf[BSIZE];
  uint indirect[NINDIRECT];
  uint x;

  rinode(inum, &din);
  off = xint(din.size);
  // printf("append inum %d at off %d sz %d\n", inum, off, n);
  while(n > 0){
    fbn = off / BSIZE;
    assert(fbn < MAXFILE);
    if(fbn < NDIRECT){
      if(xint(din.addrs[fbn]) == 0){
        din.addrs[fbn] = xint(freeblock++);
      }
      x = xint(din.addrs[fbn]);
    } else {
      if(xint(din.addrs[NDIRECT]) == 0){
        din.addrs[NDIRECT] = xint(freeblock++);
      }
      rsect(xint(din.addrs[NDIRECT]), (char*)indirect);
      if(indirect[fbn - NDIRECT] == 0){
        indirect[fbn - NDIRECT] = xint(freeblock++);
        wsect(xint(din.addrs[NDIRECT]), (char*)indirect);
      }
      x = xint(indirect[fbn-NDIRECT]);
    }
    n1 = min(n, (fbn + 1) * BSIZE - off);
    rsect(x, buf);
    bcopy(p, buf + off - (fbn * BSIZE), n1);
    wsect(x, buf);
    n -= n1;
    off += n1;
    p += n1;
  }
  din.size = xint(off);
  winode(inum, &din);
}
